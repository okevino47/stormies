/**
 * Activity animation templates for character sprites.
 * 8 new animations × 2 frames × 3 directions = 48 templates.
 * LEFT direction is auto-mirrored from RIGHT by flipHorizontal().
 *
 * Each template is a 16×24 TemplateCell grid using the same token system
 * as spriteData.ts (H=hair, K=skin, S=shirt, P=pants, O=shoes, E=eyes).
 */
// Define template cell constants locally to avoid circular dependency with spriteData.ts
const H = 'hair'
const K = 'skin'
const S = 'shirt'
const P = 'pants'
const O = 'shoes'
const E = '#FFFFFF' // eyes
const _ = ''

type TemplateCell = typeof H | typeof K | typeof S | typeof P | typeof O | typeof E | typeof _

